package az.iktlab.javasiplessons.lesson2;

public class AssignmentOperatorsExample {

    public static void main(String[] args) {

        int a = 12;
        int b = 13;

        a = a + 5; // 17
        a += 5; // 22

        b -= 5;
        a *= 2;
        b /= 7;

        System.out.println(a); // 44
        System.out.println(b); // 1



        /*
        *
        * int a = 5;
        * int b = 6;
        *
        * a = 6;
        * b = 5;
        * */
    }
}
